#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;
	unsigned int	j;

	if (!s || !(*f))
		return (NULL);
	j = 0;
	i = 0;
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	while (s[i] != '\0')
	{
		str[j] = (*f)(i, s[i]);
		i++;
		j++;
	}
	str[j] = '\0';
	return (str);
}
